# تمرین ۸: نوع مثلث
a = float(input("ضلع اول مثلث را وارد کنید: "))
b = float(input("ضلع دوم مثلث را وارد کنید: "))
c = float(input("ضلع سوم مثلث را وارد کنید: "))

if a + b > c and a + c > b and b + c > a:
    if a == b == c:
        print("مثلث متساوی‌الاضلاع است.")
    elif a == b or a == c or b == c:
        print("مثلث متساوی‌الساقین است.")
    else:
        print("مثلث مختلف‌الاضلاع است.")
else:
    print("مثلثی با این اضلاع قابل تشکیل نیست.")

